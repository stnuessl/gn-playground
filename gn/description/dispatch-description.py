import argparse
import json
import os
import sys


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument(
        'description',
        help='A description in JSON format generated by gn.',
        type=lambda x: json.load(open(x, 'r')),
    )
    parser.add_argument(
        '-o',
        help='The generated output file.',
        required=False,
        default=sys.stdout,
        type=lambda x: open(x, 'w'),
    )
    parser.add_argument(
        '--root',
        help="The project's root directory relativie to the project's build directory.",
        required=True,
        type=str,
    )

    args = parser.parse_args()

    if os.path.isabs(args.root):
        print(
            (
                f"{args.root}: error: '--root' must be relative to the "
                "project's build directory"
            ),
            file=sys.stderr,
        )
        sys.exit(1)

    # Convert gn specific path of files to absolute paths
    for item in args.description.values():
        for key in ['sources', 'inputs', 'outputs']:
            item[key] = [
                os.path.join(args.root, path.removeprefix('//'))
                for path in item.get(key, [])
            ]

    # Write output to specified file
    print(json.dumps(args.description, indent=4), file=args.o)

    sys.exit(0)


if __name__ == '__main__':
    main()
